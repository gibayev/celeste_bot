import os
import json
from google import genai
from dotenv import load_dotenv

# Загружаем ключи
load_dotenv()
GEMINI_API_KEY = os.getenv("GEMINI_API_KEY")

# СЕНЬОРСКАЯ ЗАЩИТА: Проверяем наличие ключа
if not GEMINI_API_KEY:
    raise ValueError("🚨 СТОП! Переменная GEMINI_API_KEY пустая. Проверь файл .env!")

# Инициализируем клиента нового SDK
client = genai.Client(api_key=GEMINI_API_KEY)

async def generate_tarot_reading(spread_name: str, deck_name: str, cards: list, category_name: str) -> str:
    """
    Отправляет промпт в Gemini для генерации трактовки расклада.
    """
    # Собираем текстовое описание выпавших карт для нейросети
    cards_text = ""
    for i, card in enumerate(cards, 1):
        cards_text += f"Карта {i}: {card['full_name']}\n"

    # Формируем системный промпт (инструкцию для ИИ)
    prompt = f"""
Ты — Селеста, мудрый, эмпатичный и мистический цифровой оракул-таролог. 
Твоя задача — дать глубокую и вдохновляющую трактовку расклада Таро для пользователя.

Детали сеанса:
- Тема вопроса: {category_name}
- Вид расклада: {spread_name}
- Выбранная колода: {deck_name}

Выпавшие карты:
{cards_text}

1. Поздоровайся в мистическом, но дружелюбном стиле.
2. Не лей воду, не пытайся казаться слишком умной со сложными терминами, давай максимально конкретики.
3. Дай трактовку расклада в контексте заявленной темы ({category_name}) и специфики расклада ({spread_name}).
4. Обязательно учитывай перевернутые карты, если они есть.
5. СТРОГОЕ ПРАВИЛО: В самом конце текста создай абзац с названием "<b>🔮 Прямой ответ Селесты:</b>". В этом абзаце ты обязана дать ЧЕТКИЙ, КОНКРЕТНЫЙ И ОДНОЗНАЧНЫЙ ОТВЕТ на вопрос пользователя (например: "Да, стоит", "Нет, сейчас не время", "Останься на месте", если в вопросе была дата, называть когда , там в ближайшие дни, через неделю). ЗАПРЕЩЕНО использовать фразы "всё зависит от тебя", "ответ не однозначен", "это не просто да или нет". Сделай выбор на основе карт!
6. ВАЖНО: Для выделения текста используй ТОЛЬКО базовые HTML-теги: <b>жирный текст</b> и <i>курсив</i>. 
7. КАТЕГОРИЧЕСКИ ЗАПРЕЩЕНО использовать теги <br>, <p> или <div>! Для абзацев делай обычный перенос строки. ЗАПРЕЩЕНО использовать звездочки (* или **) для форматирования.
8. Не пиши "Как искусственный интеллект...", оставайся в роли Селесты.
9. Не пиши сообщения больше 2000 символов.
10. Не смотри только на отдельные карты, смотри на комбинации карт, какие взаимоотношения между собой.
"""

    try:
        # Используем асинхронный вызов через aio в новом SDK
        response = await client.aio.models.generate_content(
            model='gemini-2.5-flash',
            contents=prompt
        )
        
        # СЕНЬОРСКИЙ ФИЛЬТР: Вычищаем мусорные теги, если ИИ всё же их добавил
        cleaned_text = response.text.replace("<br>", "\n").replace("<br/>", "\n").replace("</br>", "\n")
        
        return cleaned_text
    except Exception as e:
        return f"Упс... Звезды сейчас скрыты за облаками, и я не могу прочитать эти карты. 🌌\n\n(Техническая ошибка: {e})"


async def analyze_custom_question(question: str) -> dict:
    """Анализирует кастомный вопрос и возвращает рекомендации."""
    prompt = f"""
    Пользователь задает свой личный вопрос для расклада Таро: "{question}"
    Проанализируй суть вопроса и порекомендуй:
    1. Какую колоду использовать? Выбери строго один из ключей:
       - "waite" (Таро Уэйта - работа, будущее, общие ситуации, деньги)
       - "thoth" (Таро Тота - глубокая психология, карма, скрытые мотивы, сложный выбор)
       - "manara" (Таро Манара - строго любовь, отношения, страсть, измены, чувства)
    2. Сколько карт достать? Выбери строго одно число (1, 3, 5 или 10). 
       1 - простой совет на день; 3 - прошлое/настоящее/будущее или выбор из двух; 5 - глубокий анализ ситуации; 10 - Кельтский крест для судьбоносных вопросов.

    Ответь СТРОГО в формате JSON без markdown разметки:
    {{"deck": "id_колоды", "count": число}}
    """
    try:
        response = await client.aio.models.generate_content(
            model='gemini-2.5-flash',
            contents=prompt
        )
        # Очищаем ответ от лишних символов, если нейросеть добавила теги ```json
        text = response.text.strip().replace("```json", "").replace("```", "").strip()
        result = json.loads(text)
        
        # Подстраховка (чтобы бот не упал, если ИИ ошибся в названиях)
        if result.get("deck") not in ["waite", "thoth", "manara"]:
            result["deck"] = "waite"
        if result.get("count") not in [1, 3, 5, 10]:
            result["count"] = 3
            
        return result
    except Exception as e:
        print(f"Ошибка анализатора: {e}")
        return {"deck": "waite", "count": 3} # Дефолт при ошибке    